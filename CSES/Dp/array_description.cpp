#include <iostream>
#include <vector>

using namespace std;

int main() {
    // Хурдан оролт гаралт
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n, m;
    if (!(cin >> n >> m)) return 0;

    vector<int> x(n);
    for (int i = 0; i < n; i++) {
        cin >> x[i];
    }

    int MOD = 1e9 + 7;

    // dp[i][v] = i-р индекс дээр v утга байх боломжийн тоо
    // i нь 0-ээс n-1 хүртэл, v нь 1-ээс m хүртэл.
    // Бид санах ой хэмнэхийн тулд 2 мөр л ашиглаж болно, 
    // гэхдээ ойлгомжтой байх үүднээс бүтэн хүснэгт ашиглая.
    vector<vector<int>> dp(n, vector<int>(m + 2, 0));

    // 1. Эхний элементийг тооцоолох (Base Case)
    if (x[0] == 0) {
        // Хэрэв эхний тоо мэдэгдэхгүй бол 1..m бүгд байж болно
        for (int v = 1; v <= m; v++) {
            dp[0][v] = 1;
        }
    } else {
        // Хэрэв мэдэгдэж байвал тэр тоо л 1 боломжтой
        dp[0][x[0]] = 1;
    }

    // 2. Үлдсэн элементүүдийг тооцоолох
    for (int i = 1; i < n; i++) {
        // v нь одоогийн байрлалд байж болох утгууд (1-ээс m)
        for (int v = 1; v <= m; v++) {
            // Хэрэв оролт дээр тоо өгөгдсөн бөгөөд тэр нь v-тэй тэнцэхгүй бол
            // энэ нүд 0 байна (тооцоолох шаардлагагүй).
            if (x[i] != 0 && x[i] != v) {
                dp[i][v] = 0;
                continue;
            }

            // Өмнөх мөрийн v-1, v, v+1 утгуудын нийлбэрийг авна
            long long count = dp[i-1][v];      // Өмнөх нь ижил
            if (v - 1 >= 1) count += dp[i-1][v-1]; // Өмнөх нь 1-ээр бага
            if (v + 1 <= m) count += dp[i-1][v+1]; // Өмнөх нь 1-ээр их

            dp[i][v] = count % MOD;
        }
    }

    // 3. Хариу: Төгсгөлийн мөрөн дэх бүх боломжийн нийлбэр
    long long ans = 0;
    for (int v = 1; v <= m; v++) {
        ans = (ans + dp[n-1][v]) % MOD;
    }

    cout << ans << "\n";

    return 0;
}